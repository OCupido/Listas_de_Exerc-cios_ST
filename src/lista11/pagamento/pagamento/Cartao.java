package lista11.pagamento.pagamento;

public class Cartao {

    private String nomeDoClienete;
    private String numeroCartao;

    public String getNomeDoClienete() {
        return nomeDoClienete;
    }

    public void setNomeDoClienete(String nomeDoClienete) {
        this.nomeDoClienete = nomeDoClienete;
    }

    public String getNumeroCartao() {
        return numeroCartao;
    }

    public void setNumeroCartao(String numeroCartao) {
        this.numeroCartao = numeroCartao;
    }
}
